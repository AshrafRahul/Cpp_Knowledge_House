#include<iostream>
using namespace std;
int main(){
	
	int n1=50,n2=9,sum,sub,mul,dev,mod;
	
	sum = n1+n2;
	sub = n1-n2;
	mul = n1*n2;
	dev = n1/n2;
	mod = n1%n2;
	
	cout<<"Total sum is: "<<sum<<endl;
	cout<<"Substraction value is: "<<sub<<endl;
	cout<<"Multipication value is: "<<mul<<endl;
	cout<<"Devision value is: "<<dev<<endl;
	cout<<"Modulus value is: "<<mod<<endl;
	
	
	
	return 0;
}
